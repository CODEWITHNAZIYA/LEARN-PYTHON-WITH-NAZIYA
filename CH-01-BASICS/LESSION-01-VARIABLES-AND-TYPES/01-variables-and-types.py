# Variables are used to store data in Python.

print("========== Creating Variables ==========")

name = "Naziya"
age = 21
city = "Surat"
is_student = True

print("Name :", name)
print("Age :", age)
print("City :", city)
print("Student :", is_student)

print("\n========== Rules for Variables ==========")

student_name = "Ayaan"
studentAge = 20
_marks = 85

print(student_name)
print(studentAge)
print(_marks)

print("\n========== Multiple Variables ==========")

x, y, z = 10, 20, 30

print(x)
print(y)
print(z)

print("\n========== Change Variable Value ==========")

course = "Python"

print("Before :", course)

course = "Python Programming"

print("After :", course)

print("\n========== Variable Types ==========")

name = "Naziya"
age = 21
height = 5.4
is_learning = True

print(type(name))
print(type(age))
print(type(height))
print(type(is_learning))

